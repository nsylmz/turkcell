package com.ns.deneme.vo;

public class UIComponentVO {

	private String componentName;

	private String componentLabel;

	private String positionLeft;

	private String positionTop;

	private String componentProcessName;

	private String elementName;

	private String elementType;

	public String getComponentName() {
		return componentName;
	}

	public void setComponentName(String componentName) {
		this.componentName = componentName;
	}

	public String getPositionLeft() {
		return positionLeft;
	}

	public String getComponentLabel() {
		return componentLabel;
	}

	public void setComponentLabel(String componentLabel) {
		this.componentLabel = componentLabel;
	}

	public void setPositionLeft(String positionLeft) {
		this.positionLeft = positionLeft;
	}

	public String getPositionTop() {
		return positionTop;
	}

	public void setPositionTop(String positionTop) {
		this.positionTop = positionTop;
	}

	public String getComponentProcessName() {
		return componentProcessName;
	}

	public void setComponentProcessName(String componentProcessName) {
		this.componentProcessName = componentProcessName;
	}

	public String getElementName() {
		return elementName;
	}

	public void setElementName(String elementName) {
		this.elementName = elementName;
	}

	public String getElementType() {
		return elementType;
	}

	public void setElementType(String elementType) {
		this.elementType = elementType;
	}

}
