package com.ns.deneme.vo;

public class ViewVO {
	
	private String viewName;

	private String chart;

	private String featureBars;
	
	private String featureJson;

	public String getViewName() {
		return viewName;
	}

	public void setViewName(String viewName) {
		this.viewName = viewName;
	}

	public String getChart() {
		return chart;
	}

	public void setChart(String chart) {
		this.chart = chart;
	}

	public String getFeatureBars() {
		return featureBars;
	}

	public void setFeatureBars(String featureBars) {
		this.featureBars = featureBars;
	}

	public String getFeatureJson() {
		return featureJson;
	}

	public void setFeatureJson(String featureJson) {
		this.featureJson = featureJson;
	}
	
}
